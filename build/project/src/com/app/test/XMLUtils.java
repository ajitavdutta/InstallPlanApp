	public static void loadObjectsFromFile(File file, Object obj) {
		try {
			JAXBContext jaxbContext = JAXBContext.newInstance(obj.getClass());
			Unmarshaller unMarshaller = jaxbContext.createUnmarshaller();
			obj = unMarshaller.unmarshal(file);
		} catch (Exception e) {
			// File is empty so return a null
			e.printStackTrace();
			obj = null;
		}
	}

	public static void saveObjectListToFile(File file, PrimeCodeObjects objects) {
		try {
			JAXBContext jabxContext = JAXBContext.newInstance(PrimeCodeObjects.class);
			Marshaller marshaller = jabxContext.createMarshaller();

			marshaller.setProperty(Marshaller.JAXB_FORMATTED_OUTPUT, true);
			marshaller.marshal(objects, file);
			marshaller.marshal(objects, System.out);
		} catch (Exception e) {
			e.printStackTrace();
		}
	}

